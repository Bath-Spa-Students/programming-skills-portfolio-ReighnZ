''' This code checks if specific fruits (mango, banana, and orange) are in a list of favorite fruits.
If a fruit is found in the list, it prints a statement indicating that the person really likes that fruit.
Fruits not found in the list are not considered. '''

favorite_fruits = ["mango", "banana", "orange"]

if "mango" in favorite_fruits:
    print("You really like mangoes!")

if "banana" in favorite_fruits:
    print("You really like bananas!")

if "orange" in favorite_fruits:
    print("You really like oranges!")

if "strawberry" in favorite_fruits:
    print("You really like strawberries!")

if "apple" in favorite_fruits:
    print("You really like apples!")