''' This code demonstrates conditional statements in two versions. In the first version, it checks if an alien's color is 'green' and prints a message if true.
In the second version, the alien's color is changed to 'red', and the code doesn't print any message because the condition is not met.
It illustrates how conditions are used to make decisions based on variable values. '''

# Version 1
alien_color = 'green'
if alien_color == 'green':
    print("The player just earned 5 points.")

# Version 2
alien_color = 'red'
if alien_color == 'green':
    print("The player just earned 5 points.")