''' This code defines a dictionary called 'person_info' that holds information about an individual, including their first name, last name, age, and city.
It then proceeds to print each piece of data by accessing the corresponding keys in the dictionary.
This code serves as an example of using dictionaries to manage and display personal information efficiently. '''

person_info = {
    "first_name": "Edward",
    "last_name": "Winson",
    "age": 20,
    "city": "Manchester"
}

print("First Name:", person_info["first_name"])
print("Last Name:", person_info["last_name"])
print("Age:", person_info["age"])
print("City:", person_info["city"])